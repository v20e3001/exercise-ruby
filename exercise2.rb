s = "ruby, php, java"
p "文字列 s = #{s}"

# 特定の文字で分割して表示
p "▼ 特定の文字で分割"
p "「,」で区切る:  #{s.split(',')}"     # 「,」で区切って表示
p "「p」で区切る:  #{s.split('p')}"     # 「p」で区切って表示
p "一文字ずつ区切る:  #{s.split('')}"   # 一文字ずつ区切って表示

# 分割数の制限
p "▼ 分割数の制限"
p "分割制限なし: #{s.split('', 0)}"     # 分割制限なし
p "分割しない: #{s.split('', 1)}"       # 分割しない
p "2分割: #{s.split('', 2)}"            # 2分割(先頭文字から優先)
p "3分割: #{s.split('', 3)}"            # 3分割(先頭文字から優先)
